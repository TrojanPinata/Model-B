// MTNS
static void mountains(void) {
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_raw_P(layer0, sizeof(layer0));
            break;
        case 1:
            oled_write_raw_P(layer1, sizeof(layer1));
            break;
        default:
            oled_write_raw_P(layer0, sizeof(layer0));
            break;
    }
}

// INFO
static void info(void) {
    // get layer the board is on
    oled_write_P(PSTR("LAYER "), false);
    oled_write_P(PSTR(" "), true);
    oled_write_char(get_highest_layer(layer_state) + 0x30, true);
    oled_write_P(PSTR(" "), true);

    // here we do a bit of trickery
    oled_write_P(PSTR(" ENC "), false);
    oled_write_P(PSTR(" VOL "), true);

    // lock key statuses
    led_t led_state = host_keyboard_led_state();
    oled_set_cursor(9, 1);
    oled_write_P(PSTR(" CAP "), led_state.caps_lock);
    oled_set_cursor(14, 1);
    oled_write_P(PSTR(" SCR "), led_state.scroll_lock);

    // write model B here in invert text
    oled_set_cursor(0, 3);
    oled_write_P(PSTR(" MODEL-B "), true);

    // mod masks - if you don't know what these do, don't worry about them
    uint8_t mod_state = get_mods();
    oled_advance_char();
    oled_advance_char();
    oled_write_P(PSTR("S"), mod_state & MOD_MASK_SHIFT);
    oled_advance_char();
    oled_write_P(PSTR("C"), mod_state & MOD_MASK_CTRL);
    oled_advance_char();
    oled_write_P(PSTR("A"), mod_state & MOD_MASK_ALT);
    oled_advance_char();
    oled_write_P(PSTR("G"), mod_state & MOD_MASK_GUI);
    oled_advance_char();
}

// display logo - used on boot and exception state
static void logo(void) {
    oled_write_raw_P(bootscreen, sizeof(bootscreen));
}

// logo, but also have some signifier for the current layer
static void logo_with_layer(void) {
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_raw_P(bootscreen, sizeof(bootscreen));
            break;
        case 1:
            oled_write_raw_P(invertlogo, sizeof(invertlogo));
            break;
        default:
            oled_write_raw_P(bootscreen, sizeof(bootscreen));
            break;
    }
}

// run this whenever the oled needs to update
bool oled_task_user(void) {
    user_config.raw = eeconfig_read_user(); // read saved config
    display_mode = user_config.display_mode_saved;
    switch (display_mode) {
        case MTNS:
            mountains();
            break;
        case INFO:
            info();
            break;
        case LOGO:
            logo_with_layer();
            break;
        default:
            logo();
    }
    return false;
}

// what happens on sleep/power down
void suspend_power_down_user(void) {
    oled_off();
}

// render this on boot/reset
void oled_render_boot(bool bootloader) {
    oled_clear();
    logo();
    oled_render_dirty(true);
}

// run this on boot/reset
bool shutdown_user(bool jump_to_bootloader) {
    oled_render_boot(jump_to_bootloader);
    return false;
}

// change depending on if OLED_MOD is pressed
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (keycode == OLED_MOD) {
        if (record->event.pressed) {
            oled_clear();
            if (display_mode == NUM_MODES - 1) {
                display_mode = 0;
            }
            else {
                display_mode++;
            }
            user_config.display_mode_saved = display_mode;
            eeconfig_update_user(user_config.raw);  // update config when screen changes
        }
    }
    return true;
}

